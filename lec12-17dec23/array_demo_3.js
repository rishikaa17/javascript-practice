//if we have an array empty or having some value and we want to add some value to it we can do that 
let arr = [1,2,3,4,5,6];
console.log(arr);
arr.push(10);//it adds 10 to the last of the array 
console.log(arr);
arr.unshift(-1);
console.log(arr);

//remove the element from the start of an array or 0th index
 
arr.shift();
console.log(arr);

//if you want to remove the element from the last 

arr.pop();
console.log(arr);